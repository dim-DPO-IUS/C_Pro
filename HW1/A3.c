#include <stdio.h>
#include <stdint.h>

int main(void) {
    uint32_t n;
    unsigned int k;
    
    // Читаем входные данные
    if (scanf("%u %u", &n, &k) != 2) {
        return 1;
    }
    
    // Проверяем, что K в диапазоне [1, 31]
    if (k < 1 || k > 31) {
        // Если K вне диапазона, можно установить в максимальное значение
        k = 31;
    }
    
    // Маска для K младших битов
    // Пример для K=3: 1<<3 = 8, 8-1 = 7 (бинарное 111)
    // Пример для K=5: 1<<5 = 32, 32-1 = 31 (бинарное 11111)
    uint32_t mask;
    
    // Аккуратно создаем маску, чтобы избежать сдвига на 32 бита
    if (k == 32) {
        mask = 0xFFFFFFFF;  // Все 32 бита установлены в 1
    } else {
        mask = (1u << k) - 1;
    }
    
    // Извлекаем K младших битов
    uint32_t result = n & mask;
    
    printf("%u\n", result);
    
    return 0;
}
